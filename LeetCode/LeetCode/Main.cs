        public void processProgramm()
        {
            Console.WriteLine("-----Программа для демонстраций задач с leetcode-----");
            if (easyTasks.Count == 0 && mediumTasks.Count == 0 && hardTasks.Count == 0)
            {
                Console.WriteLine("Еще нет решенных задач с leetcode!");
                return;
            }
            while (true)
            {
                int choiceUserDifficult = askUserDifficult();
                if (choiceUserDifficult == codeToExit)
                {
                    return;
                }
                List<InfoBasicTask> selectedGroupTasks = getTasksByDifficult(choiceUserDifficult);
                if (selectedGroupTasks.Count == 0)
                {
                    Console.WriteLine("В данной группе нет решенных задач. Выберите другую группу!");
                    continue;
                }
                selectedGroupTasks = sortTaskInGroupByNumber(selectedGroupTasks);
                printTasksByDifficult(selectedGroupTasks);
                while (true)
                {
                    int numberTaskFromUser = askUserNumberTask();
                    if (numberTaskFromUser == codeToExit)
                    {
                        break;
                    }
                    InfoBasicTask selectedTask = getSelectedTaskByNumber(selectedGroupTasks, numberTaskFromUser);
                    if (selectedTask == null)
                    {
                        Console.WriteLine("Введен номер несуществующей задачи! Повторите попытку!");
                        continue;
                    }
                    Console.WriteLine($"-----Задача №{selectedTask.getNumber()}-----");
                    Console.WriteLine($"Наименование задачи: {selectedTask.getName()}");
                    if (selectedTask.getDescription() != String.Empty)
                    {
                        Console.WriteLine($"Описание задачи: {selectedTask.getDescription()}");
                    }
                    int actionTask = askUserToShowResultTask();
                    switch (actionTask)
                    {
                        case (int)ActionOnTask.executeTask:
                            selectedTask.execute();
                            break;
                        case (int)ActionOnTask.cancel:
                            break;
                        case (int)ActionOnTask.testing:
                            try
                            {
                                selectedTask.testing();
                            }
                            catch (NotImplementedException)
                            {
                                Console.WriteLine($"Тестирование для задачи №{selectedTask.getNumber()} не реализовано!");
                            }
                            break;
                    }
                    Console.WriteLine("-----------------");
                }
            }
        }
        private int askUserDifficult()
        {
            while (true)
            {
                Console.WriteLine("Выберите уровень сложности:\n" +
                    "1 - легкий\n" +
                    "2 - средний\n" +
                    "3 - сложный\n" +
                    "0 - завершить работу приложения");
                Console.Write("Ваш выбор: ");
                try
                {
                    int choiceUser = Int32.Parse(Console.ReadLine());
                    if (choiceUser < 0 || choiceUser > 3)
                    {
                        throw new FormatException();
                    }
                    return choiceUser;
                }
                catch (FormatException)
                {
                    Console.WriteLine("Введено неверное значение. Повторите попытку!");
                }
            }
        }
        private List<InfoBasicTask> getTasksByDifficult(int numberDifficult)
        {
            List<InfoBasicTask> selectedGroupTasks = new List<InfoBasicTask>();
            switch (numberDifficult)
            {
                case (int)Difficult.easy:
                    return easyTasks;
                case (int)Difficult.medium:
                    return mediumTasks;
                case (int)Difficult.hard:
                    return hardTasks;
                default:
                    return null;
            }
        }
        private void printTasksByDifficult(List<InfoBasicTask> tasks)
        {
            for (int i = 0; i < tasks.Count; i++)
            {
                Console.WriteLine($"Номер задачи №{tasks[i].getNumber()} - {tasks[i].getName()}");
            }
        }
        private int askUserNumberTask()
        {
            while (true)
            {
                Console.Write("Введите номер задачи (введите 0 для возврата): ");
                try
                {
                    int choiceUser = Int32.Parse(Console.ReadLine());
                    if (choiceUser < 0)
                    {
                        throw new FormatException();
                    }
                    return choiceUser;
                }
                catch (FormatException)
                {
                    Console.WriteLine("Введено неверное значение. Повторите попытку!");
                }
            }
        }
        private InfoBasicTask getSelectedTaskByNumber(List<InfoBasicTask> selectedGroupTasks, int numberTask)
        {
            for (int i = 0; i < selectedGroupTasks.Count; i++)
            {
                if (selectedGroupTasks[i].getNumber() == numberTask)
                {
                    return selectedGroupTasks[i];
                }
            }
            return null;
        }

        private int askUserToShowResultTask()
        {
            while (true)
            {
                Console.WriteLine("Хотите ли вы получить решение задачи (1 - да, 2 - нет, 3 - только показать результаты внутреннего тестирования)?");
                Console.Write("Ваш выбор: ");
                try
                {
                    int choiceUser = Int32.Parse(Console.ReadLine());
                    if (choiceUser < 1 || choiceUser > 3)
                    {
                        throw new FormatException();
                    }
                    return choiceUser;
                }
                catch (FormatException)
                {
                    Console.WriteLine("Вы ввели неверное значение. Повторите попытку!");
                }
            }
        }
        private List<InfoBasicTask> sortTaskInGroupByNumber(List<InfoBasicTask> tasks)
        {
            return tasks.OrderBy(task => task.getNumber()).ToList();
        }
        private void sortTasksByDifficult()
        {
            for (int i = 0; i < allTasks.Count; i++)
            {
                switch (allTasks[i].getDifficult())
                {
                    case Difficult.easy:
                        easyTasks.Add(allTasks[i]);
                        break;
                    case Difficult.medium:
                        mediumTasks.Add(allTasks[i]);
                        break;
                    case Difficult.hard:
                        hardTasks.Add(allTasks[i]);
                        break;
                }
            }
        }
